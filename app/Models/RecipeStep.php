<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class RecipeStep extends Model
{
    /*
     * ============================================================================================================================================
     * Settings
     * ============================================================================================================================================
     */
    // no need time stamps on this
    public $timestamps = false;
    
    
    /*
     * ============================================================================================================================================
     * Relationships
     * ============================================================================================================================================
     */
    public function recipe()
    {
        return $this->belongsTo('App\Models\Recipe');
    }
}
